private void getfirstrecord() {
    if(admintable.getRowCount() > 0)
        admintable.setRowSelectionInterval(0, 0);
}
    

private void getlastrecord() {
    int last = admintable.getRowCount() - 1;
    if(last >= 0) admintable.setRowSelectionInterval(last, last);
}
    

    private void getnextrecord() {
    int selectedRow = admintable.getSelectedRow();
    if(selectedRow < admintable.getRowCount() - 1)
        admintable.setRowSelectionInterval(selectedRow + 1, selectedRow + 1);
}
    

    private void getpreviousrecord() {
    int selectedRow = admintable.getSelectedRow();
    if(selectedRow > 0)
        admintable.setRowSelectionInterval(selectedRow - 1, selectedRow - 1);
}
    

        private void getdisplayrecords() {
    try {
        DefaultTableModel model = (DefaultTableModel) admintable.getModel();
        model.setRowCount(0); // clear table

        pst = cn.prepareStatement("SELECT * FROM users");
rs = pst.executeQuery();
while(rs.next()) {
    Object[] row = {
        rs.getInt("user_id"),
        rs.getString("name"),
        rs.getString("email"),
        rs.getString("phone"),
        rs.getString("user_type")
    };
    model.addRow(row);
}
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error displaying the records: " + ex.getMessage());
    }
}
