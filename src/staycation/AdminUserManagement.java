        private void getaddbutton() {
    try {
        String nameVal = namefield.getText().trim();
        String emailVal = emailfield.getText().trim();
        String phoneVal = phonefield.getText().trim();
        String userTypeVal = (String) jComboBox1.getSelectedItem();
        String passwordVal = String.valueOf(passwordfield.getPassword());

        if(nameVal.isEmpty() || emailVal.isEmpty() || phoneVal.isEmpty() || passwordVal.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all fields");
            return;
        }

        pst = cn.prepareStatement("INSERT INTO login ( name, email, phone, user_type, password) VALUES (?, ?, ?, ?, ?)");
        pst.setString(1, nameVal);
        pst.setString(2, emailVal);
        pst.setString(3, phoneVal);
        pst.setString(4, userTypeVal);
        pst.setString(5, passwordVal);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(null, "User added successfully!");
        getdisplayrecords(); // refresh table

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error adding user: " + ex.getMessage());
    }
}

    

    private void getdeleterecords() {
        try {
        int selectedRow = admintable.getSelectedRow();
        if(selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Select a row to delete");
            return;
        }
        int id = (Integer) admintable.getValueAt(selectedRow, 0);
        pst = cn.prepareStatement("DELETE FROM login WHERE user_id=?");
        pst.setInt(1, id);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(null, "User deleted successfully!");
        getdisplayrecords();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error deleting user: " + ex.getMessage());
    }
}
   

    private void getupdate() {
    try {
        int selectedRow = admintable.getSelectedRow();
        if(selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Select a row to update");
            return;
        }

        int id = (Integer) admintable.getValueAt(selectedRow, 0);
        String nameVal = namefield.getText().trim();
        String emailVal = emailfield.getText().trim();
        String phoneVal = phonefield.getText().trim();
        String userTypeVal = (String) jComboBox1.getSelectedItem();
        String passwordVal = String.valueOf(passwordfield.getPassword());

       pst = cn.prepareStatement(
    "UPDATE users SET name=?, email=?, phone=?, user_type=? WHERE user_id=?");
    pst.setString(1, nameVal);
    pst.setString(2, emailVal);
    pst.setString(3, phoneVal);
    pst.setString(4, userTypeVal);
    pst.setInt(5, id);
    pst.executeUpdate();


        JOptionPane.showMessageDialog(null, "User updated successfully!");
        getdisplayrecords();

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error updating the user: " + ex.getMessage());
    }


    }

    private void getsearch() {
        try {
        String searchName = namefield.getText().trim();
        DefaultTableModel model = (DefaultTableModel) admintable.getModel();
        model.setRowCount(0);

        pst = cn.prepareStatement("SELECT * FROM login WHERE name LIKE ?");
        pst.setString(1, "%" + searchName + "%");
        rs = pst.executeQuery();

        while(rs.next()) {
            Object[] row = {
                rs.getInt("user_id"),
                rs.getString("name"),
                rs.getString("email"),
                rs.getString("phone"),
                rs.getString("user_type")
            };
            model.addRow(row);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error searching user: " + ex.getMessage());
    }
}
   

